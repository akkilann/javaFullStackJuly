package com.training.basic;

public class IncDecOperator {
    public static void main(String[] args){
        int a = 10;
        int b = 20;

        int c = 30;
        int d = 40;

        a++;b++;
        System.out.println(a);
        System.out.println(b);
        --c;--d;
        System.out.println(c);
        System.out.println(d);
    }
}
